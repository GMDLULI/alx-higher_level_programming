def add(a, b):
    """My additon funtion

    Args:
        a:  first interger
        b: second interger

    Returns:
        the return value. a + b
    """
    return (a + b)


def sub(a, b):
    """My additon funtion

    Args: 
        a: first interger
        b: second interger

    Returns:
        the return value. a - b
    """
    return (a - b)


def mul(a, b):
    """My additon funtion

    Args: 
        a: first interger
        b: second interger
	
    Returns:
        the return value. a * b
    """
    return (a * b)


def div(a, b):
    """My additon funtion

    Args: 
        a: first interger
        b: second interger

    Returns:
        the return value. a / b
    """
    return int(a / b)
